package principal;

public class relojAlarma {
	private int horaAl;
	private int horaAc;
	private int minAl;
	private int minAc;
	private boolean modoReloj;

	public void sethoraAl(int horaAl) {
		this.horaAl = horaAl;
	}

	public int gethoraAl() {
		return horaAl;
	}

	public void sethoraAc(int horaAc) {
		this.horaAc = horaAc;
	}

	public int gethoraAc() {
		return horaAc;
	}

	public void setminAl(int minAl) {
		this.minAl = minAl;
	}

	public int getminAl() {
		return minAl;
	}

	public void setminAc(int minAc) {
		this.minAc = minAc;
	}

	public int getminAc() {
		return minAc;
	}

	public void setmodoReloj(boolean modoReloj) {
		this.modoReloj = modoReloj;
	}

	public boolean getmodoReloj() {
		return modoReloj;
	}

}
